export function getService(slug: string): Service | undefined {
  return SERVICES.find((s) => s.slug === slug);
}

/** Профессия в творительном падеже для «Работа {…}» */
export function workNoun(service: Service): string {
  switch (service.role) {
    case 'picker':
      return 'сборщиком заказов';
    case 'driver':
      return 'водителем';
    case 'shift':
      return 'на сменах';
    default:
      return 'курьером';
  }
}

/** Короткое существительное для карточек «Похожие вакансии» */
export function roleShort(service: Service): string {
  switch (service.role) {
    case 'picker':
      return 'Сборщик';
    case 'driver':
      return 'Водитель';
    case 'shift':
      return 'Смены';
    default:
      return 'Курьер';
  }
}

/** За что платят: подставляется в «оплата за {…}» */
export function payUnit(service: Service): string {
  switch (service.role) {
    case 'picker':
    case 'shift':
      return 'смены';
    case 'driver':
      return 'выполненные поездки';
    default:
      return 'выполненные заказы';
  }
}

